using UnityEngine;

public class MonoSlave : MonoBehaviour
{
    public static MonoSlave Instance { get; private set; }
    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}